let opr=prompt("enter an operator")
let x=10; let y=20
switch(opr){

    case "+":{
        alert(x+y)
        break
    }
    case "-":{
        alert(x-y)
        break
    }
    case "*":{
        alert(x*y)
        break
    }
    default:{
        alert("invalid operation")
    }

}